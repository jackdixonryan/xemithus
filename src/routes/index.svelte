	<h2>The Story</h2>
	<p>In the year 2059, after the re-allotment sessions of the early 2030s, the Xemithus Prosperity Group found themselves with parsec after parsec of empty space, which the global consortium now declared a part of the new frontier. Desperation took hold, for now the Prosperity Group's dreams of a unified empire were in tatters; They had to sell the land, otherwise square parsecs of it would be taken at will from the global consortium, and for what? Galactic Park Space?</p>
	<p>After some turbulent restructuring, the XPG reformed into the Xemithus Economic Authority and came up with a simple ploy: as the central issuer of their token, they could continue to hold and control the economy of their vast territory. With this revelation, the XEA contract board was born and the XEA issued its first stock of ZTokens, which would be awarded to anyone who sought to transform the millions of parsecs into the group's dream of a shining intergalactic universe.</p>
	<p>Prosperity soars in Xemithus, and you have the opportunity to take part. Build cool shit, settle new lands, and become a member of the thriving economy.</p>
	<p>That is the story so far; the rest is up to you. The history of the galaxy will be determined by your actions. What will you choose to do?</p>
