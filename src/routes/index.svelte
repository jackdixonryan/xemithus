	<h2>Introduction</h2>
	<p>Welcome to Xemithus! The objective of this application is for you to establish an empire in the vastness of space. Whether you choose to work with or against other titans of industry is your decision. Step out into the void and create something for yourself.</p>

	<p>Someday, Xemithus will run on the blockchain. One of the main objectives of this application is to showcase the very probable future of smart contracts and smart payment systems. In Xemithus, you will be able to issue and fulfill contracts for other players in the space. This could be anything, from destroying a rival colony to exploring an unknown quadrant. Contracts are issued and the Xemithus Economic Authority determines internally how much they should be worth.</p>

	<p>You will be paid on your completion of those contracts, but beware: The more ZTokens the contract promises, the more dangerous it is.</p>
