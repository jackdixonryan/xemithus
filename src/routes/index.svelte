	<h2>The Tech</h2>

	<p>Xemithus's Frontend is run on <a href="https://github.com/sveltejs/sapper">Sapper from Svelte</a>, a super-cool, wicked fast web framework that cuts way down on how much energy we'll ask your computer to use. Go give them your stars. The backend is a pretty straightforward node.js API, and is where all the cool shit happens.</p>

	<p>Eventually, we'll be using some web six protocols when we go to release Xemithus Ethereum, which will use the Ethereum blockchain to process the transactions on this site. In the meantime, we'll be using plain jane Javascript to showcase the basic functionality, tracking our Ztokens and contracts through clear, transparent rails. The open economy is here!</p>
